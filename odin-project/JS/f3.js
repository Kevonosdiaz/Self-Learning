function add7(num) {
  return num + 7;
}
let multiply = (a, b) => a * b;
let capitalize = (s) => s.charAt(0).toUpperCase();
let lastLetter = (s) => s.charAt(s.length - 1);

console.log(add7(12));
console.log(multiply(7, 9));
console.log(capitalize("hi my name is Kevin"));
console.log(capitalize("A"));
console.log(lastLetter("Hello world!"));
