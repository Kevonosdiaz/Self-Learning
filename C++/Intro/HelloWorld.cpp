// A simple Hello World program, just printing out "Hello World"
#include <iostream> // Like an import statement for Input/Output (IO) functions
using namespace std; // Import iostream with name std

/**
 * Function returning an int
 * Print out the string "Hello World" as output
*/
int main() {
    cout << "Hello World" << endl; // << are insertion operators, endl outputs a new line after the string
    return 0; // Return 0 as convention (things are fine)
}